    
    #include p16f84.inc
    
    #DEFINE BOUTON PORTA,0
    COMPT1 EQU 0x0D
    COMPT2 EQU 0x0E
    
BANK0 MACRO
    BCF STATUS,RP0
    ENDM
    
BANK1 MACRO
    BSF STATUS,RP0
    ENDM
    
    ORG 0x00
    GOTO START
    
START ORG 0x05
    BANK1
    MOVLW 0xFF
    MOVWF TRISA
    MOVLW 0x00
    MOVWF TRISB
    BANK0
    MOVLW 0x00
    MOVWF PORTA
    MOVLW 0x00
    MOVWF PORTB

BOUCLE
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWONE
    
SHOWONE
    MOVLW 0x06
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    CALL ATTENTE
    GOTO SHOWTWO
    
SHOWTWO
    MOVLW 0x5B
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    CALL ATTENTE
    GOTO SHOWTHREE
    
SHOWTHREE
    MOVLW 0x4F
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    CALL ATTENTE
    GOTO SHOWFOUR
    
SHOWFOUR
    MOVLW 0x66
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    CALL ATTENTE
    GOTO SHOWFIVE
    
SHOWFIVE
    MOVLW 0x6D
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    CALL ATTENTE
    GOTO SHOWSIX
    
SHOWSIX
    MOVLW 0x7D
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    CALL ATTENTE
    GOTO SHOWONE
    
ATTENTE
    MOVLW 0x40
    MOVWF COMPT1
    MOVLW 0x40
    MOVWF COMPT2
    
DELAI1
DELAI
    DECFSZ COMPT2
    GOTO DELAI
    
    DECFSZ COMPT1
    GOTO DELAI1
    
    RETURN
    
END
