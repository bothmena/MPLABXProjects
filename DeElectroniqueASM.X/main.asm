    
    #include p16f84.inc
    
    #DEFINE BOUTON PORTA,0
    
BANK0 MACRO
    BCF STATUS,RP0
    ENDM
    
BANK1 MACRO
    BSF STATUS,RP0
    ENDM
    
    ORG 0x00
    GOTO START
    
START ORG 0x05
    BANK1
    MOVLW 0xFF
    MOVWF TRISA
    MOVLW 0x00
    MOVWF TRISB
    BANK0
    MOVLW 0x00
    MOVWF PORTA
    MOVLW 0x00
    MOVWF PORTB

BOUCLE
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWONE
    
SHOWONE
    MOVLW 0x06
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWTWO
    
SHOWTWO
    MOVLW 0x5B
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWTHREE
    
SHOWTHREE
    MOVLW 0x4F
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWFOUR
    
SHOWFOUR
    MOVLW 0x66
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWFIVE
    
SHOWFIVE
    MOVLW 0x6D
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWSIX
    
SHOWSIX
    MOVLW 0x7D
    MOVWF PORTB
    BTFSS BOUTON
    GOTO BOUCLE
    GOTO SHOWONE
    
END
